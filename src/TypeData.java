public class TypeData {
    public static void main(String[] args) {
        byte typeByte = 100;
        short typeShort = 100;
        int typeInt = 100;
        long typeLong = 100;
        long typeLong2 = 100;
        float typeFloat = 3.4F;
        double typeDouble = 1.7;
        System.out.println("Type data Number: " + typeByte);

        char j = 'j';
        char o = 'o';
        char n = 'n';
        System.out.println("Type data Char: " + j + o + n);

        boolean benar = true;
        boolean salah = false;
        System.out.println("Type data Boolean: " + benar);

        String firstName = "Jon";
        String lastName = "Heri";
        System.out.println("Type data String: " + firstName + " " + lastName);

    }
}
